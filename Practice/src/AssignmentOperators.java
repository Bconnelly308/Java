public class AssignmentOperators {
	public static void main(String[] args) {
		int a = 10; // assigns a the value 10
	    System.out.println(a);
		int b = 10;
	    b += 5; // adds 5 to the value of b (10)
	    System.out.println(b);
	    int c = 10;
	    c -= 5; // subtracts 5 from the value of c (10)
	    System.out.println(c);
	    int d = 10;
	    d *= 5; // multiples 5 to the value of d (10)
	    System.out.println(d);
	    int e = 10;
	    e /= 5; // divides 5 from the value of e (10)
	    System.out.println(e);
	    int f = 10;
	    f %= 5; // modulus 5 from the value of f (10)
	    System.out.println(f);
	    int g = 5;
	    g &= 3; 
	    System.out.println(g);
	    int h = 5;
	    h |= 3;
	    System.out.println(h);
	    int i = 5;
	    i ^= 3;
	    System.out.println(i);
	    int j = 5;
	    j >>= 3;
	    System.out.println(j);
	    int k = 5;
	    k <<= 3;
	    System.out.println(k);
	}
}
