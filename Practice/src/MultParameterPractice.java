
public class MultParameterPractice {
	public static void parameterPractice2(String fname, int age) {
		System.out.println(fname + " is " + age);
	}
	public static void main(String[] args) {
		parameterPractice2("Hannah", 16);
		parameterPractice2("Jennifer", 14);
		parameterPractice2("Dennis", 51);
		parameterPractice2("Jake", 50);
	}
}